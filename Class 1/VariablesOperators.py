'''Arithmetic'''
x = 5
y = 3
print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x**y) #exponentiaton
print(x//y) #floor division
print(x%y) #modulo

'''Assignment'''
x = 4
x += 2 # x = x + 2
x -= 2 # x = x - 2
  
'''Comparision'''
print(x==y)
print(x!=y) #not equal
print(x<y)
print(x>y)
print(x<=y)
print(x>=y)

'''Logical'''
print(False and True)
print(True or False)
print(not False)
print(x!=y and x<y)

'''Identity'''
print(x is y)